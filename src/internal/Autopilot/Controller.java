    private float capThrust(float desiredThrust){
        float minThrust = 0f;
        float maxThrust = this.getConfig().getMaxThrust();

        if(desiredThrust < minThrust){
            return minThrust;
        }
        if(desiredThrust > maxThrust){
            return maxThrust;
        }

        return desiredThrust;
    }

